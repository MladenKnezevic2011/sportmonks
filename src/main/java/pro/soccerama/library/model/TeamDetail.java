package pro.soccerama.library.model;

public class TeamDetail 
{
	private int id = -1;
	private String name = null;
	private String logo_path = null;
	
	public TeamDetail() {}
	
	public int getId() {
		return id;
	}
	public String getName() {
		return name;
	}
	public String getLogo() {
		return logo_path;
	}
}
